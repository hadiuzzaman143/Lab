class MultiplyTwoNumbers
{
    public static void main(String[] args)
    {
         // f is to ensures that numbers are float DATA TYPE
        float a = 1.5f;
        float b = 2.0f;
         // to store the multiplied value
        float product = a * b;
        // to print the product
        System.out.println("The product is: " + product);
    }
}
